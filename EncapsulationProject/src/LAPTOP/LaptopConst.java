package LAPTOP;

class LaptopConst {
	private String processor;
	private int memorysize;
	public LaptopConst(String processor, int memorysize)
	{
		this.processor=processor;
		this.memorysize=memorysize;
		
	}
	public String getprocessor()
	{
		return processor;
	}
	public int getmemorysize()
	{
		return memorysize;
	}
	

}
